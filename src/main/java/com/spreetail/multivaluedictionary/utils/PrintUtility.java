package com.spreetail.multivaluedictionary.utils;

/* PrintUtility Class provides logging functionality */
public class PrintUtility {

    /* print() method logs messages to the console
    * @param message to be logged on the console
    * @return no return value. Input is logged to the console */
    public static void print(String message) {
        System.out.println(message);
    }
}
